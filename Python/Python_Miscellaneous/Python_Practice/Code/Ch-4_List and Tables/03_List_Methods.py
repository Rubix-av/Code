# Creating a list
l1 = [6,5,7,3,1,2,4,8,9]
print(l1)

# Sorting list in ascending order
l1.sort()
print("Sort",l1)

# Sorts the list from the [-1] index
l1.reverse()
print("reverse",l1)

# Adds at the end of the list ( *ONE OF THE MOST USED LISTS FUNCTION* )
l1.append(8)
print("append",l1)

# It inserts specified number on the specified index number
l1.insert(0, 334)
print("insert",l1)

# It pops out the number on the specified index number
l1.pop(4)
print("pop",l1)

# It removes the specified number
l1.remove(2)
print("remove",l1)   

