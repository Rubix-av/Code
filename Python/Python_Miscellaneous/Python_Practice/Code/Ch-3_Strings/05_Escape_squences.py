name1 = ("1) Today is thursday. \nTomorrow is firday.") # '\n' prints the characters after it in next line
name2 = ("2) I am good. I \tam very good.") # '\t' will print the characters after it in the next line
name3 = ("3) Akshobh\'s book is in good condition") # "\'" this will add single quote in the line
name4 = ("4) I need to have pen\\pencil to do the test") # '\\' will add a backslash in the line

# Printing the outcomes of above python lines 
print(name1)
print(name2)
print(name3)
print(name4)