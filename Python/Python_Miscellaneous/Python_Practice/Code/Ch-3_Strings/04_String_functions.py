poem = "there was a crow he was thirsty, he thought a plan"

# String Functions
print(len(poem)) # This counts the number of characters in the string
print(poem.endswith("plan")) # This ensures if the end character or string is correct or not
print(poem.count("th")) # It counts the number of a string or characters
print(poem.capitalize()) # It capatalizes the 1st word in the string
print(poem.find("was")) # It outputs only the first occurance of the word
print(poem.replace("crow", "bull")) # It replaces all the words in the string
