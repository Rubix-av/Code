age = int(input("Enter your age: "))

# True if both operands are true else false
if(age > 20 and age < 50):
    print("You are eligible for the job")

else:
    print("You are not eligible for the job")

# True if at least one operand is true else false
if(age > 20 and age < 50):
    print("You are eligible for the job")

else:
    print("You are not eligible for the job")

# Inverts true to false and false to true
if(age > 20 and age < 50):
    print("You are eligible for the job")

else:
    print("You are not eligible for the job")
